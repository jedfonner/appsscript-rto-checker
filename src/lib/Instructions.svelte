<p>
  This app pulls your Google Calendar and computes all the days where you set your Working
  Location to be in the office.
</p>
<p>
  It will <u>not work</u> if you do not use the Working Location feature in Google Calendar.
  See
  <a
    href="https://support.google.com/calendar/answer/7638168?hl=en&co=GENIE.Platform%3DDesktop"
    target="_blank">here</a
  > for how to set up your Working Location in Google Calendar.
</p>
<p>
  It will <u>also not work</u> if you are not diligent about updating your Working Location in Google
  Calendar when you deviate from your normal in-office schedule.
</p>
